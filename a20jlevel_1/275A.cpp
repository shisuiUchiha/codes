#include<bits/stdc++.h>
using namespace std;


#define ll long long


int main(){
	int b[3][3];
	int a[3][3];
	for(int i=0;i<3;i++){
		for(int j=0;j<3;j++){
			cin>>a[i][j];
			if(a[i][j]%2==0){
				a[i][j]=0;
			}
			else{
				a[i][j]=1;
			}
			b[i][j]=1;
		}
	}
	if(a[0][0]==1){
		b[0][0]=0^(b[0][0]);
		b[0][1]=0^(b[0][1]);
		b[1][0]=0^(b[1][0]);
	}
	if(a[0][1]==1){
		b[0][1]=0^(b[0][1]);
		b[0][2]=0^(b[0][2]);
		b[0][0]=0^(b[0][0]);
		b[1][1]=0^(b[1][1]);
	}
	if(a[0][2]==1){
		b[0][2]=0^(b[0][2]);
		b[0][2]=0^(b[0][2]);
		b[1][2]=0^(b[1][2]);
	}
	if(a[1][0]==1){
		b[1][0]=0^(b[1][0]);
		b[0][0]=0^(b[0][1]);
		b[1][1]=0^(b[1][1]);
		b[2][0]=0^(b[2][0]);
	}
	if(a[1][1]==1){
		b[1][1]=0^(b[1][1]);
		b[1][0]=0^(b[1][0]);
		b[1][2]=0^(b[1][2]);
		b[0][1]=0^(b[0][1]);
		b[2][1]=0^(b[2][1]);
	}
	if(a[1][2]==1){
		b[1][2]=0^(b[1][2]);
		b[2][2]=0^(b[2][2]);
		b[0][2]=0^(b[0][2]);
		b[1][1]=0^(b[1][1]);
	}
	if(a[2][0]==1){
		b[2][0]=0^(b[2][0]);
		b[2][1]=0^(b[2][1]);
		b[1][0]=0^(b[1][0]);
	}
	if(a[2][1]==1){
		b[2][2]=0^(b[2][2]);
		b[2][1]=0^(b[2][1]);
		b[1][1]=0^(b[1][1]);
		b[2][0]=0^(b[2][0]);
	}
	if(a[2][2]==1){
		b[2][2]=0^(b[2][2]);
		b[2][1]=0^(b[2][1]);
		b[1][2]=0^(b[1][2]);
	}

	for(int i=0;i<3;i++){
		for(int j=0;j<3;j++){
			cout<<b[i][j];
		}
		cout<<endl;
	}
}